package com.vc.collections.model;

public class Entry<K, T> {
    private K key;
    private T value;
    private Entry next;

    public Entry() {
    }

    public Entry(K key, T value, Entry next) {
        this.key = key;
        this.value = value;
        this.next = next;
    }

    public K getKey() {
        return key;
    }

    public void setKey(K key) {
        this.key = key;
    }

    public T getValue() {
        return value;
    }

    public void setValue(T value) {
        this.value = value;
    }

    public Entry getNext() {
        return next;
    }

    public void setNext(Entry next) {
        this.next = next;
    }
}
